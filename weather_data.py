def get_heat_days_by_region(region_name, month=None):
    """
    지역명과 월을 입력받아 혹서기 비작업일수 반환
    
    Args:
        region_name (str): 지역명 (예: "서울", "부산")
        month (int or str, optional): 월 (1-12 또는 "1월"-"12월")
    
    Returns:
        float or dict: 특정 월이 주어지면 해당 월 값, 없으면 전체 딕셔너리
    """
    if region_name not in HEAT_DAYS:
        # 기본값: 서울
        region_name = "서울"
    
    data = HEAT_DAYS[region_name]
    
    if month is not None:
        if isinstance(month, int):
            month_key = f"{month}월"
        else:
            month_key = month
        return data.get(month_key, 0.0)
    
    return data

def get_total_non_work_days(region_name, start_month, end_month):
    """
    지역과 기간을 입력받아 총 비작업일수 계산 (혹서기만)
    
    Args:
        region_name (str): 지역명
        start_month (int): 시작 월 (1-12)
        end_month (int): 종료 월 (1-12)
    
    Returns:
        float: 총 비작업일수
    """
    total = 0.0
    data = get_heat_days_by_region(region_name)
    
    if start_month <= end_month:
        for m in range(start_month, end_month + 1):
            total += data.get(f"{m}월", 0.0)
    else:
        # 연도를 넘어가는 경우
        for m in range(start_month, 13):
            total += data.get(f"{m}월", 0.0)
        for m in range(1, end_month + 1):
            total += data.get(f"{m}월", 0.0)
    
    return total